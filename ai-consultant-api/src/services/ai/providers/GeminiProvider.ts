        if (errorMessage.includes('401') || errorMessage.includes('403') ||
            errorMessage.includes('API_KEY_INVALID') || errorMessage.includes('PERMISSION_DENIED')) {
          throw new Error(
            'üîí –û—à–∏–±–∫–∞ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ Gemini API\n\n' +
            '–ö–æ–¥ –æ—à–∏–±–∫–∏: 401/403 (Unauthorized/Permission Denied)\n' +
            '–ü—Ä–∏—á–∏–Ω–∞: –ù–µ–≤–µ—Ä–Ω—ã–π API –∫–ª—é—á –∏–ª–∏ –¥–æ—Å—Ç—É–ø –∑–∞–ø—Ä–µ—â–µ–Ω.\n\n' +
            '–ß—Ç–æ –¥–µ–ª–∞—Ç—å:\n' +
            '‚Ä¢ –ü—Ä–æ–≤–µ—Ä—å—Ç–µ, —á—Ç–æ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è GEMINI_API_KEY —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞ –ø—Ä–∞–≤–∏–ª—å–Ω–æ\n' +
            '‚Ä¢ –°–æ–∑–¥–∞–π—Ç–µ –Ω–æ–≤—ã–π API –∫–ª—é—á –Ω–∞ https://aistudio.google.com/app/apikey\n' +
            '‚Ä¢ –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –∫–ª—é—á –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å "AIza"\n' +
            '‚Ä¢ –í–ê–ñ–ù–û: API –∫–ª—é—á –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –æ—Ç —Ç–æ–≥–æ –∂–µ Google –∞–∫–∫–∞—É–Ω—Ç–∞, —á—Ç–æ –∏ –ø—Ä–æ–µ–∫—Ç'
          );
        }

